package org.bitbucket.sikorrr.cv.data;

public class PersonalInfo {

  private String webPage;
  private String phoneNumber;
  private String email;
  private String circularImageUrl;
  private String headerBackgroundImage;
  private String name;

  public PersonalInfo() {
  }

  public String getWebPage() {
    return webPage;
  }

  public String getPhoneNumber() {
    return phoneNumber;
  }

  public String getEmail() {
    return email;
  }

  public String getHeaderBackgroundImage() {
    return headerBackgroundImage;
  }

  public String getCircularImageUrl() {
    return circularImageUrl;
  }

  public String getName() {
    return name;
  }
}
